saldo = 100
#usando o operador += para soma
saldo+=50 #equivale saldo +50
print(saldo) #saida é de= 150.

#usando o operador -= para subtraçao
saldo-=25 #equivale saldo -25
print(saldo) #saida é de= 125.

#usando o operador *= para multiplicaçao
estoque = 10
estoque *=2 #equivale estoque *2
print(estoque) #saida é de= 20

#usando o operador /= para dividir
total_vendas =500
total_vendas /=2 #equivale total_vendas/ 2
print(total_vendas) #saida é de= 250.